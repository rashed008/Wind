import UIKit

extension UIFont {
    class func CircularBoldFont(ofSize: CGFloat) -> UIFont {
        return UIFont(name: "CircularStd-Bold", size: ofSize)!
    }
    
    class func CircularMediumFont(ofSize: CGFloat) -> UIFont {
        return UIFont(name: "Circula-Medium", size: ofSize)!
    }
}

